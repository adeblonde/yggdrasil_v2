### Common commands to run for git 

### Loop during code writing

### commit
git commit

### push
git push -u origin branch

### fetch
git fetch

### rebase to dev
git rebase dev

### switch to dev branch
git checkout dev

### get last version of dev branc
git pull origin dev

### merge from task branch
git merge task --no-ff

### push back the merged dev branch
git push origin dev

### remove branch
git branch -d branch

git push origin :branch

git prune